    public EmojiPackage ToEmojiPackage() {
        EmojiPackage package = new EmojiPackage {
            Author = "system",
            AlbumCover = "pack://application:,,,/Emoji;Component/images/defaults/emoji_type_5@2x.png",
            AlbumSelectedCover = "pack://application:,,,/Emoji;Component/images/defaults/emoji_type_selected_5@2x.png",
            Title = "Symbols",
            Items = new List<EmojiItem>()
        };
        package.Items.AddRange(this.items);

        return package;
    }

    public Dictionary<string, EmojiItem> EmojiToIcoDictionary() {
        EmojiItem[] symbolsItems = this.items.ToArray();
        Dictionary<string, EmojiItem > dic = symbolsItems.ToDictionary(key => key.Code, value => value);

        return dic;
    }

    public Dictionary<string, EmojiItem> IcoToEmojiDictionary() {
        EmojiItem[] symbolsItems = this.items.ToArray();
        Dictionary<string, EmojiItem> dic = symbolsItems.ToDictionary(key => key.ImgPath, value => value);

        return dic;
    }
